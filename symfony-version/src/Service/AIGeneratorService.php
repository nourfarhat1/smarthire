    private function categorizeJobTitle(string $jobTitle): string
    {
        $keywords = [
            'IT/Software' => ['developer', 'programmer', 'software', 'engineer', 'technical', 'IT'],
            'Marketing' => ['marketing', 'digital', 'social media', 'brand', 'advertising'],
            'Sales' => ['sales', 'business development', 'account', 'revenue'],
            'HR' => ['human resources', 'HR', 'recruiting', 'personnel'],
            'Finance' => ['finance', 'financial', 'accounting', 'analyst', 'investment']
        ];

        foreach ($keywords as $category => $words) {
            foreach ($words as $word) {
                if (stripos(strtolower($jobTitle), $word) !== false) {
                    return $category;
                }
            }
        }
        
        return 'General';
    }

    private function detectExperienceLevel(string $jobTitle): string
    {
        $seniorKeywords = ['senior', 'lead', 'manager', 'director', 'head', 'principal', 'chief'];
        $juniorKeywords = ['junior', 'entry', 'associate', 'assistant', 'coordinator'];
        
        $title = strtolower($jobTitle);
        
        foreach ($seniorKeywords as $keyword) {
            if (stripos($title, $keyword) !== false) {
                return 'Senior Level';
            }
        }
        
        foreach ($juniorKeywords as $keyword) {
            if (stripos($title, $keyword) !== false) {
                return 'Entry Level';
            }
        }
        
        return 'Mid Level';
    }

    public function generateEditingTips(string $title, string $category, string $description, string $tone = 'professional'): array
    {
        $prompt = $this->buildEditingTipsPrompt($title, $category, $description, $tone);
        
        try {
            // Use curl directly to bypass SSL verification issues
            $ch = curl_init();
            
            // Try direct API with a simpler model first
            $data = [
                'inputs' => $prompt,
                'parameters' => [
                    'max_length' => 512,
                    'temperature' => 0.7
                ]
            ];
            
            curl_setopt_array($ch, [
                CURLOPT_URL => 'https://api-inference.huggingface.co/models/distilbert-base-uncased',
                CURLOPT_RETURNTRANSFER => true,
                CURLOPT_POST => true,
                CURLOPT_POSTFIELDS => json_encode($data),
                CURLOPT_HTTPHEADER => [
                    'Content-Type: application/json'
                ],
                CURLOPT_SSL_VERIFYPEER => false,
                CURLOPT_SSL_VERIFYHOST => false,
                CURLOPT_TIMEOUT => 30,
                CURLOPT_VERBOSE => true
            ]);
            
            $response = curl_exec($ch);
            $httpCode = curl_getinfo($ch, CURLINFO_HTTP_CODE);
            curl_close($ch);
            
            if ($response === false) {
                return $this->generateFallbackEditingTips($title, $category, $description, $tone);
            }
            
            $result = json_decode($response, true);
            
            if ($httpCode !== 200 || !isset($result[0]['generated_text'])) {
                return $this->generateFallbackEditingTips($title, $category, $description, $tone);
            }
            
            $aiTips = $result[0]['generated_text'];
            
            // Parse AI response into structured tips
            return $this->parseEditingTips($aiTips, $title, $category, $tone);
            
        } catch (\Exception $e) {
            return $this->generateFallbackEditingTips($title, $category, $description, $tone);
        }
    }

