    public function generateTrainingDescription(string $title, string $category, string $tone = 'professional'): string
    {
        $prompt = $this->buildPrompt($title, $category, $tone);
        
        try {
            // Use curl directly to bypass SSL verification issues
            $ch = curl_init();
            
            // Try direct API with a simpler model first
            $data = [
                'inputs' => $prompt,
                'parameters' => [
                    'max_length' => 1024,
                    'temperature' => 0.7
                ]
            ];
            
            curl_setopt_array($ch, [
                CURLOPT_URL => 'https://api-inference.huggingface.co/models/distilbert-base-uncased',
                CURLOPT_RETURNTRANSFER => true,
                CURLOPT_POST => true,
                CURLOPT_POSTFIELDS => json_encode($data),
                CURLOPT_HTTPHEADER => [
                    'Content-Type: application/json',
                    'Authorization: Bearer ' . $this->huggingFaceApiKey
                ],
                CURLOPT_SSL_VERIFYPEER => false,
                CURLOPT_SSL_VERIFYHOST => false,
                CURLOPT_TIMEOUT => 30,
                CURLOPT_VERBOSE => true
            ]);
            
            // Capture verbose output for debugging
            $verbose = fopen('php://temp', 'w+');
            curl_setopt($ch, CURLOPT_STDERR, $verbose);
            
            $response = curl_exec($ch);
            $error = curl_error($ch);
            $httpCode = curl_getinfo($ch, CURLINFO_HTTP_CODE);
            
            if ($error) {
                throw new \RuntimeException('cURL error: ' . $error);
            }
            
            if ($httpCode !== 200) {
                throw new \RuntimeException('HTTP error: ' . $httpCode . ' Response: ' . $response);
            }
            
            curl_close($ch);
            
            // Log verbose output for debugging
            rewind($verbose);
            $verboseLog = stream_get_contents($verbose);
            fclose($verbose);
            
            // Log the verbose output to error log for debugging
            error_log('Hugging Face API verbose output: ' . $verboseLog);
            error_log('Hugging Face API response: ' . $response);
            
            $content = json_decode($response, true);
            if (json_last_error() !== JSON_ERROR_NONE) {
                throw new \RuntimeException('JSON decode error: ' . json_last_error_msg());
            }
            
            // Handle direct API response format
            if (isset($content[0]['generated_text'])) {
                return $content[0]['generated_text'];
            }
            
            // Handle chat completion format (fallback)
            if (isset($content['choices'][0]['message']['content'])) {
                return $content['choices'][0]['message']['content'];
            }

            throw new \RuntimeException('No content generated by AI. Response: ' . $response);

        } catch (\Exception $e) {
            // Fallback to template-based description when AI fails
            return $this->generateFallbackDescription($title, $category);
        }
    }

