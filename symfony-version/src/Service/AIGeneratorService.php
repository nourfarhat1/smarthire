    public function generateSuggestedSalary(string $jobTitle, string $location): string
    {
        $prompt = "Based on the job title '{$jobTitle}' in '{$location}', suggest an appropriate annual salary range. Consider factors like experience level required, industry standards, and local market conditions. Provide the salary as a specific range (e.g., '$45,000 - $65,000') or a single representative figure (e.g., '$55,000').";

        try {
            // Use curl directly to bypass SSL verification issues
            $ch = curl_init();
            
            $data = [
                'model' => 'adzuna-llama-2-7b-chat',
                'messages' => [
                    [
                        'role' => 'user',
                        'content' => $prompt
                    ]
                ],
                'max_tokens' => 150,
                'temperature' => 0.3
            ];
            
            curl_setopt_array($ch, [
                CURLOPT_URL => 'https://api.adzuna.com/v1/chat/completions',
                CURLOPT_RETURNTRANSFER => true,
                CURLOPT_POST => true,
                CURLOPT_POSTFIELDS => json_encode($data),
                CURLOPT_HTTPHEADER => [
                    'Content-Type: application/json'
                ],
                CURLOPT_SSL_VERIFYPEER => false,
                CURLOPT_SSL_VERIFYHOST => false,
                CURLOPT_TIMEOUT => 30,
                CURLOPT_VERBOSE => true
            ]);
            
            $response = curl_exec($ch);
            $httpCode = curl_getinfo($ch, CURLINFO_HTTP_CODE);
            curl_close($ch);
            
            if ($response === false) {
                return $this->generateFallbackSalary($jobTitle, $location);
            }
            
            $result = json_decode($response, true);
            
            if ($httpCode !== 200 || !isset($result['choices'][0]['message']['content'])) {
                return $this->generateFallbackSalary($jobTitle, $location);
            }
            
            $suggestedSalary = $result['choices'][0]['message']['content'];
            
            // Clean up the response to get just the salary figure
            if (preg_match('/\$?([0-9,]+(?:\s*-\s*[0-9,]+)?\s*\$?/', $suggestedSalary, $matches)) {
                return $matches[1];
            }
            
            return $suggestedSalary;
            
        } catch (\Exception $e) {
            return $this->generateFallbackSalary($jobTitle, $location);
        }
    }

