    }

    private function parseEditingTips(string $aiTips, string $title, string $category, string $tone): array
    {
        // Try to parse AI response into structured tips
        $lines = explode("\n", $aiTips);
        $tips = [];
        
        foreach ($lines as $line) {
            $line = trim($line);
            
            // Look for numbered items or bullet points
            if (preg_match('/^\d+\.\s*(.+)/', $line, $matches)) {
                $tips[] = trim($matches[1]);
            } elseif (preg_match('/^[•\-\*]\s*(.+)/', $line, $matches)) {
                $tips[] = trim($matches[1]);
            } elseif (!empty($line) && strlen($line) > 5) {
                $tips[] = $line;
            }
        }
        
        // If we couldn't parse AI response, use fallback
        if (count($tips) < 3) {
            return $this->generateFallbackEditingTips($title, $category, $description, $tone);
        }
        
        return [
            'tips' => array_slice($tips, 0, 4),
            'source' => 'ai',
            'confidence' => 'high'
        ];
    }

